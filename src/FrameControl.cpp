float FrameControl::GetDeltaTime() const
{
	return mdeltaTime;
}

void FrameControl::Init()
{
	mprevTime = Tangerine::Get_Time();
}

void FrameControl::Update()
{
	mdeltaTime = Tangerine::Get_Time() - mprevTime;
	mprevTime = Tangerine::Get_Time();
}
