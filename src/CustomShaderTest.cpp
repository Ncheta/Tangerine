CustomShaderTest::CustomShaderTest(Shader* baseShader) :mBaseShader(nullptr)
{
	if (baseShader)
	{
		mBaseShader = baseShader;
	}
}

void CustomShaderTest::SetCustomUniforms()
{
	if (mBaseShader)
	{
		mBaseShader->SetFloat("u_time", Tangerine::Get_Time());
	}
}
